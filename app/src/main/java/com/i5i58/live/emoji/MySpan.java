package com.i5i58.live.emoji;

public class MySpan {

	Object span;
	
	int start;
	
	int end;
	
	int flag;

	public Object getSpan() {
		return span;
	}

	public void setSpan(Object span) {
		this.span = span;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}
	
	
}
